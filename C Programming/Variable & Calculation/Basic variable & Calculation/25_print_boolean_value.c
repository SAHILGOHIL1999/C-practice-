#include <stdio.h>
#include <stdbool.h>


    // Note: C does not have a built-in Boolean type, you can use <stdbool.h>

int main()
{
    // Accept a Boolean Value and Print It
    // printing a boolean using %d prints 1 for true and 0 for false.
    bool val = true;
    printf ("Value is %d\n", val);

    bool val1 = false;
    printf ("Value is %d\n", val1);

    return 0;
}

